#creamos la variable i con valor 0 para mas adelante contar los numeros positivos.
i=0
#creamos casos para que se introduzcan la cantidad de numeros a poner.
casos=int(input())
#creamos el for con la variable x y metemos casos dentro del range
for x in range(casos):
    #creamos una variable diferente a la de for porque no se puede cambiar y le ponemos su int,input
    n=int(input())
    #este if es para que siempre que el numero sea mayor a 0 cuente como numero positivo.
    if n>0:
        #con la variable i se hace el conteo y se va sumando +1 por cada numero mayor a 0
        i+=1
#imprimimos i para saber el total de los numeros positivos ingresados.        
print(i)