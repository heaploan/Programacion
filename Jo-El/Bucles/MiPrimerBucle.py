#creamos la variable casos con un int,input para asi introducir la cantidad de datos que se tendran que introducir
casos=int(input())
#creamos el for con una variable llamada n para que guarde los datos ingresados por el usuario cierta cantidad de veces
for n in range(casos):
    #le ponemos el int,input a la variable n para ingresar los datos.
    n=int(input())
    #creamos la variable resultat para que haga la suma de la variable n+1
    resultat=n+1
    #imprimimos el resultado.
    print(resultat)
