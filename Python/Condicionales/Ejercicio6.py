'''Demana a l’usuari la base i alçada d’un rectangle i mostra el seu
perímetre i l’àrea.'''

base=float(input("Introduce la base de un rectángulo: "))
altura=float(input("Introduce la altura de un rectángulo: "))
print("El área rectángulo es: ",base*altura)
print("El perímetro del rectángulo es: ",base*2+altura*2)
